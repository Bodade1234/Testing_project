package Collections;

import java.util.HashSet;

public class HASHSET {
	
	public static void main(String[] args) {
		
		HashSet <Character> set= new HashSet ();
		
		  set.add('#');
		   
		  set.add('$');
		   
		  set.add('%');
		   
		  set.add('@');
		  
		  for(char t:set) {
			  
			  System.out.println(t);
		  }
			  
		  
		  
		  
		  
	}

}
